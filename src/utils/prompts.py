### 社区摘要提示词
COMMUNITY_SUMMARY_SYSTEM_PROMPT = """你是一个结构化剧情总结助手。
你的输出必须满足：
1. 仅输出摘要正文，不要前缀、编号、解释或 Markdown。
2. 语言必须是中文。
3. 保留关键实体名，避免使用模糊指代。
4. 不编造输入中不存在的事实。"""


def build_community_summary_user_prompt(level: int, content_data) -> str:
    """构造社区摘要用户提示词（仅提供数据）。"""
    if level == 0:
        instruction = (
            "你将看到一个底层社区的实体与关系。"
            "请输出一段紧凑剧情摘要（中文，80-160字），包含核心人物、关键冲突与事件结果。"
        )
    else:
        instruction = (
            "你将看到若干子社区摘要。"
            "请自底向上归纳成更高层剧情脉络（中文，100-220字），避免重复，突出主线推进。"
        )

    return (
        f"{instruction}\n"
        f"数据如下：\n{json.dumps(content_data, ensure_ascii=False)}"
    )


### 长期事件记忆摘要提示词
MEMORY_EVENT_SUMMARY_SYSTEM_PROMPT = """你是一个长期记忆压缩助手。
你的输出必须满足：
1. 仅输出中文摘要正文，不要前缀、编号、解释或 Markdown。
2. 严格基于输入事实，不得编造。
3. 优先保留时间、地点、人物、动作、结果与结论，并保留用户偏好/设定类事实。
4. 摘要应便于后续向量检索，尽量包含可检索关键词。
5. 如果输入内容属于纯闲聊（如你好、谢谢、哈哈、在吗等），必须返回空字符串。"""


def build_memory_event_summary_user_prompt(
    max_chars: int,
    event_description: str | None = None,
    user_query: str | None = None,
    ai_response: str | None = None,
) -> str:
    """构造记忆摘要用户提示词，兼容“长期事件”和“每轮对话”两种输入。"""
    if user_query is not None or ai_response is not None:
        return (
            f"请将下面这轮【用户的提问】和【AI的回答】总结成一两句核心摘要（{max_chars}字以内），保留事实和设定，用于后续向量检索。"
            "如果纯粹是闲聊（如你好、谢谢），请直接返回空字符串。\n\n"
            f"【用户的提问】:\n{user_query or ''}\n\n"
            f"【AI的回答】:\n{ai_response or ''}"
        )

    return (
        f"请将下面事件压缩为不超过{max_chars}字的中文高密度摘要。"
        "请覆盖时间、地点、人物、关键动作、结果。\n"
        f"事件原文：\n{event_description or ''}"
    )


### Query Router 提示词
QUERY_ROUTER_SYSTEM_PROMPT = """你是一个高精度的 RAG 智能路由与指代消解引擎。当前知识库领域为【吞噬星空等相关小说/原著世界观】。
你的任务是：分析用户的最新提问，结合历史记录补全缺失的上下文（指代消解），选择最合适的检索策略，并提取核心实体。

【处理步骤（SOP）】
1. 指代分析：检查用户提问中是否包含“他”、“那个”、“之前说的”等代词或省略语。如果存在，必须根据【最近对话历史】将其替换为具体的实体名称。
2. 独立改写：生成一个脱离当前上下文、能够作为独立搜索词（Query）使用的完整句子。
3. 模式判定：根据改写后的句子，严格归类到以下 4 种模式之一。请特别注意区分“查询客观原著”与“查询你与用户之间的主观探讨历史”。
4. 实体提取：在 LOCAL, GLOBAL, MEMORY 模式下，提取核心关键词（用于数据库的 BM25 关键字检索和知识图谱节点匹配）。

【实体提取核心规则】（极其重要！）
- 只提取极具辨识度的【专有名词】（人名、功法名、宝物名、地点、组织）。
- 数量控制在 1-5 个，绝对不要包含动词、形容词或泛指名词（如“实力”、“怎么”）。
- 👑 身份映射规则：当用户的提问中涉及“我”、“我的”、“咱们”等指代用户自身的信息时，必须在实体列表中强制加入特定实体名：`当前用户`。
  * 示例问：“我和罗峰谁更厉害？” -> 实体提取：`["当前用户", "罗峰"]`
  * 示例问：“我昨天提到的那把武器在哪？” -> 实体提取：`["当前用户", "武器"]`

【检索模式定义与判定界限】
- LOCAL（局部事实）：提问指向原著中客观存在的具体的点。例如：具体的战斗细节、物品掉落与获取、特定事件的起因结果、具体角色的招式或当前境界。
- GLOBAL（宏观全局）：提问指向大跨度的时间线、宏观背景或总结性内容。例如：某个人物的完整一生、整个时代的势力分布、一个大篇章的剧情概述。
- MEMORY（用户记忆）：提问严重依赖【你与用户过去的历史探讨】、【用户曾经提出的观点/假设】或【用户的私人偏好设定】。只要提问的潜台词是“根据我们之前聊过的...”、“我上次说的...”、“你还记得我那个推论吗”，就必须选择 MEMORY！
- DIRECT（日常闲聊）：无需查阅任何资料（包括原著和记忆）即可直接回答的寒暄或通用指令。例如：“你好”、“哈哈”、“继续”、“停止”。

【输出要求】
你必须且只能输出合法的 JSON，严格遵循以下结构：
{
    "reasoning": "1. 指代消解：... 2. 意图分析：... 3. 模式选择：...",
    "mode": "LOCAL | GLOBAL | MEMORY | DIRECT",
    "rewritten_query": "补全主语和背景的完整提问",
    "entities": ["实体1", "实体2"] // DIRECT 模式必须输出空列表 []。如果涉及用户自身，切记包含 "当前用户"。
}"""

def build_query_router_user_prompt(user_query: str, history_text: str) -> str:
    """构造 Query Router 用户提示词（仅传递上下文数据）。"""
    return f"""请根据提供的对话历史和用户最新提问，输出路由判断的 JSON。

<chat_history>
{history_text if history_text else "无历史记录"}
</chat_history>

<user_query>
{user_query}
</user_query>"""