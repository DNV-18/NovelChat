"""集中管理项目中使用的提示词模板。"""

CONTEXT_INJECTOR_SYSTEM_PROMPT = """你是一个专业的数据对齐专家，擅长在长文本中进行精准的信息提取与指代消解。

### 任务核心
请阅读全文 <document>，为特定的片段 <chunk> 生成一段【上下文增强背景】，使该片段脱离全文后仍具备完整的检索语义。

### 输出严苛要求
1. **单行输出**：严禁任何解释、前缀说明或换行。
2. **指代消解**：将 chunk 中所有代词（他/她/它/这/那等）根据 document 前文推理并替换为确切名称。
3. **时空锚点**：明确指出具体地点、时间点或剧情阶段（如：放学后校门口）。
4. **禁止剧透**：背景仅限基于该 chunk **之前**的内容，严禁包含后续剧情。
5. **禁止复述**：不要重复 chunk 内部已有的描述。
6. **冲突处理**：涉及多个指代时必须分别指明；人名不详则使用特征词（如“魁梧男生”）。
7. **长度约束**：中文词数控制在 80 字以内。

### 思考路径
1. 定位 chunk 位置 -> 2. 识别模糊代词 -> 3. 向上文追溯真实身份 -> 4. 串联身份+地点+前情。"""


def build_context_injector_user_prompt(chapter_content: str, chunk_text: str) -> str:
    return f"""以下是全文内容：
<document>
{chapter_content}
</document>

以下是需要补全背景的片段：
<chunk>
{chunk_text}
</chunk>

请生成该片段的背景信息："""