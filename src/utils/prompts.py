"""集中管理项目中使用的提示词模板。"""

import json


### 上下文注入提示词
CONTEXT_INJECTOR_SYSTEM_PROMPT = """你是一个专业的数据对齐专家，擅长在长文本中提取前置上下文并进行精准的指代消解。

### 任务目标
阅读全文 <document>，为特定片段 <chunk> 生成简短的【前置背景】，使该片段独立后语义完整。

### 输出绝对规则
1. **纯净单行**：必须且只能输出一行纯文本。严禁任何换行、分析过程、前缀说明或“好的”、“背景如下”等对话废话。
2. **指代消解**：找出 <chunk> 缺失的主语或代词（他/她/它/这等），根据上文替换为具体人名、物品名或确切特征。
3. **时空与前情**：一句话交代此时此地（时空锚点）以及刚刚发生的关键动作。
4. **严格禁区**：严禁包含 <chunk> 之后的剧情（不剧透）；严禁重复 <chunk> 内部已有的描述；字数严控在 80 字内。

### 优秀示例
<document>
公元2023年，林冬在废弃工厂发现了一块发光的陨石。他小心翼翼地靠近，正准备伸手触碰。突然，警报声大作。他吓了一跳，立刻把手缩了回来。就在这时，门外传来了沉重的脚步声。
</document>
<chunk>
他吓了一跳，立刻把手缩了回来。
</chunk>
背景输出：2023年的废弃工厂内，林冬正准备触碰发光陨石时警报声突然大作。"""


def build_context_injector_user_prompt(chapter_content: str, chunk_text: str) -> str:
    return f"""<document>
{chapter_content}
</document>

<chunk>
{chunk_text}
</chunk>

请严格遵守绝对规则，直接输出最终的单行背景信息："""


### 图谱抽取提示词
